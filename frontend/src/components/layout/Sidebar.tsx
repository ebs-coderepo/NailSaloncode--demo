'use client';

import Link from 'next/link';
import { usePathname, useRouter } from 'next/navigation';
import { useTransition } from 'react';

type NavItem = { href: string; label: string; icon: (p: { className?: string }) => React.ReactElement };

// Items visible to OWNER and MANAGER
const OWNER_NAV: NavItem[] = [
  { href: '/dashboard',              label: 'Overview',     icon: HomeIcon },
  { href: '/dashboard/services',     label: 'Services',     icon: SparklesIcon },
  { href: '/dashboard/staff',        label: 'Staff',        icon: UsersIcon },
  { href: '/dashboard/appointments', label: 'Appointments', icon: CalendarIcon },
  { href: '/dashboard/customers',    label: 'Customers',    icon: PersonIcon },
  { href: '/dashboard/calendar',     label: 'Calendar',     icon: CalViewIcon },
  { href: '/dashboard/reviews',      label: 'Reviews',      icon: StarIcon },
  { href: '/dashboard/finance',      label: 'Finance',      icon: FinanceIcon },
  { href: '/dashboard/voice',        label: 'Voice AI',     icon: MicIcon },
  { href: '/dashboard/users',        label: 'Team Accounts', icon: KeyIcon },
  { href: '/dashboard/settings',     label: 'Settings',     icon: SettingsIcon },
];

// Items visible to STAFF only
const STAFF_NAV: NavItem[] = [
  { href: '/dashboard/calendar',  label: 'My Calendar', icon: CalViewIcon },
  { href: '/dashboard/services',  label: 'Services',    icon: SparklesIcon },
  { href: '/dashboard/staff',     label: 'My Profile',  icon: PersonIcon },
];

export default function Sidebar({
  userName,
  userEmail,
  userRole,
}: {
  userName: string;
  userEmail?: string;
  userRole: string;
}) {
  const pathname   = usePathname();
  const router     = useRouter();
  const [pending, start] = useTransition();

  const isStaff  = userRole === 'STAFF';
  const navItems = isStaff ? STAFF_NAV : OWNER_NAV;
  const initials = userName.split(' ').map((n) => n[0]).join('').toUpperCase().slice(0, 2);

  async function handleLogout() {
    start(async () => {
      await fetch('/api/auth/logout', { method: 'POST' });
      router.push('/login');
      router.refresh();
    });
  }

  return (
    <aside className="flex flex-col w-64 min-h-screen bg-white border-r border-gray-200 px-4 py-6">

      {/* Logo */}
      <div className="flex items-center gap-2 px-3 mb-8">
        <div className="w-8 h-8 rounded-lg bg-brand-600 flex items-center justify-center flex-shrink-0">
          <MicIcon className="w-4 h-4 text-white" />
        </div>
        <div>
          <p className="text-sm font-semibold text-gray-900 leading-none">AI Receptionist</p>
          <p className="text-xs text-gray-500 mt-0.5 capitalize">{userRole.toLowerCase()} portal</p>
        </div>
      </div>

      {/* Navigation */}
      <nav className="flex flex-col gap-1 flex-1">
        {navItems.map(({ href, label, icon: Icon }) => {
          const isActive =
            href === '/dashboard'
              ? pathname === '/dashboard'
              : pathname === href || pathname.startsWith(href + '/');
          return (
            <Link
              key={href}
              href={href}
              className={`nav-link ${isActive ? 'active' : ''}`}
            >
              <Icon className="w-4 h-4 flex-shrink-0" />
              {label}
            </Link>
          );
        })}
      </nav>

      {/* User footer */}
      <div className="mt-auto pt-4 border-t border-gray-200 flex flex-col gap-2">
        <div className="flex items-center gap-3 px-3 py-2">
          <div className="w-8 h-8 rounded-full bg-brand-100 flex items-center justify-center flex-shrink-0">
            <span className="text-xs font-semibold text-brand-700">{initials}</span>
          </div>
          <div className="min-w-0 flex-1">
            <p className="text-sm font-medium text-gray-900 truncate">{userName}</p>
            {userEmail && <p className="text-xs text-gray-500 truncate">{userEmail}</p>}
          </div>
        </div>
        <button
          onClick={handleLogout}
          disabled={pending}
          className="nav-link text-red-500 hover:text-red-700 hover:bg-red-50"
        >
          <LogoutIcon className="w-4 h-4 flex-shrink-0" />
          {pending ? 'Signing out…' : 'Sign out'}
        </button>
      </div>
    </aside>
  );
}

